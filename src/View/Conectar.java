private void guardarConexion(String nombre, Config cfg) {
    try {
        File file = new File("conexiones.json");
        JSONArray jsonArray = new JSONArray();

        if (file.exists()) {
            String contenido = new String(Files.readAllBytes(file.toPath()), StandardCharsets.UTF_8);
            if (!contenido.isEmpty()) {
                jsonArray = new JSONArray(contenido);
            }
        }

        JSONObject json = new JSONObject();
        json.put("nombre", nombre);
        json.put("host", cfg.getHost());
        json.put("ruta", cfg.getRuta());
        json.put("user", cfg.getUser());
        json.put("pass", cfg.getContra());

        jsonArray.put(json);


        try (FileWriter fw = new FileWriter(file)) {
            fw.write(jsonArray.toString(2)); 
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

    private Config leerForm() {
        String nombre = txtNombre.getText().trim();
        String host = txtHost.getText().trim();
        String ruta = txtRuta.getText().trim();
        ruta = ruta.replace('\\','/');
        String usuario = txtUser.getText().trim();
        String password = new String(txtContra.getPassword());
        return new Config(nombre, host, ruta, usuario, password);
    }


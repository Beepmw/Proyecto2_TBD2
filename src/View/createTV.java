    private void actualizarDDL() {
        if(mdlCol == null){
            return;
        }
        String nombre = txtNombreTV.getText().trim();
        if (nombre.isEmpty()) {
            tAddl.setText("-- Escribe un nombre para continuar");
            return;
        }

        String tipo = (String) cmbTV.getSelectedItem();

        if ("Table".equalsIgnoreCase(tipo)) {
            StringBuilder ddl = new StringBuilder("CREATE TABLE " + nombre + " (\n");
            Vector<Vector> data = mdlCol.getDataVector();
            for (int i = 0; i < data.size(); i++) {
                Vector fila = mdlCol.getDataVector().get(i);
                String colNombre = fila.get(0).toString();
                String colTipo = fila.get(1).toString();
                String conPres = fila.get(2).toString();
                boolean notNull = Boolean.parseBoolean(fila.get(3).toString());
                boolean pk = Boolean.parseBoolean(fila.get(4).toString());

                ddl.append("  ").append(colNombre).append(" ").append(colTipo);
                if (!conPres.isEmpty() && 
                ("VARCHAR".equalsIgnoreCase(colTipo) || 
                 "CHAR".equalsIgnoreCase(colTipo) || 
                 "DECIMAL".equalsIgnoreCase(colTipo))) {
                ddl.append("(").append(conPres).append(")");
            }

            if (notNull) ddl.append(" NOT NULL");
            if (pk) ddl.append(" PRIMARY KEY");

            if (i < data.size() - 1) ddl.append(",");
            ddl.append("\n");
            }
            ddl.append(");");
            tAddl.setText(ddl.toString());

        } else if ("View".equalsIgnoreCase(tipo)) {
            String sqlSelect = tASelect.getText().trim();
            if (sqlSelect.isEmpty()) {
                tAddl.setText("-- Escribe el SELECT para la vista");
            } else {
                tAddl.setText("CREATE VIEW " + nombre + " AS\n" + sqlSelect + ";");
            }
        }
    }

    public void agregarColumna(String nombre, String tipo, String longitud, boolean notNull, boolean pk) {
        mdlCol.addRow(new Object[]{nombre, tipo, longitud, notNull, pk});
        actualizarDDL();
    }


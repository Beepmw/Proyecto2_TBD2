    private void btnGuardarNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarNewActionPerformed
        String ddl = tAddl.getText().trim();
        if (ddl.isEmpty() || ddl.startsWith("--")) {
            JOptionPane.showMessageDialog(this, "Error", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        TreePath path = log.getJTree().getSelectionPath(); 
        if (path == null || path.getPathCount() < 2) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una conexion", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nombreConexion = path.getPathComponent(1).toString();
        ConexionIB conexion = log.getGestor().getConexion(nombreConexion); 
        if (conexion == null) {
            JOptionPane.showMessageDialog(this, "Error", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            
            if (conexion.getConnection() == null || conexion.getConnection().isClosed()) {
                conexion.estaAbierta();
            }
            Connection con = (Connection) conexion.getConnection();
            try (java.sql.Statement st = con.createStatement()) {
                st.executeUpdate(ddl); 
                JOptionPane.showMessageDialog(this, "Creacion exitosa", "Exito", JOptionPane.INFORMATION_MESSAGE);
            }

            log.actualizarArbol();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error en el create: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        tAddl.setText("");
    }//GEN-LAST:event_btnGuardarNewActionPerformed

